//IMPORT MONGOOSE
const { Schema, Types } = require('mongoose');

//NOT A MODEL, JUST A SCHEMA TO BE USED IN THOUGHTS
const reactionSchema = new Schema(
  {
    reactionId: {
      type: Schema.Types.ObjectId,
      default: () => new Types.ObjectId(),
    },
    reactionBody: {
      type: String,
      required: true,
      maxLength: 280,
    },
    username: {
      type: String,
      required: true,
    },
    createdAt: {
      type: Date,
      default: Date.now,
    },
  },
  {
    toJSON: {
      getters: true,
    },
    id: false,
  }
);

//EXPORT MODULE
module.exports = reactionSchema